----1. CREATE DIRECTORY IN DATABASE
CREATE DIRECTORY MASOUD_DUMPS AS '/Backups';
SELECT DIRECTORY_PATH FROM DBA_DIRECTORIES WHERE DIRECTORY_NAME = 'MASOUD_DUMPS';
----2. CREATE TABLESPACE IN DATABASE
SELECT TABLESPACE_NAME FROM DBA_TABLESPACES;

CREATE TABLESPACE TBS_EXPDP DATAFILE SIZE 1M AUTOEXTEND ON MAXSIZE UNLIMITED;
CREATE USER MASOUD_EXPDP IDENTIFIED BY PASSWORD DEFAULT TABLESPACE TBS_EXPDP QUOTA UNLIMITED ON TBS_EXPDP PROFILE MASOUD_DBA_USERS_PROFILE;
----3. GRANT SOME PRIVILEGES
GRANT READ, WRITE ON DIRECTORY DUMPS TO MASOUD_EXPDP;
GRANT DATAPUMP_EXP_FULL_DATABASE TO MASOUD_EXPDP;
####

######################################################
mkstore -wrl $ORACLE_HOME/admin/wallets -create
mkstore -wrl $ORACLE_HOME/admin/wallets -createCredential MASOUD MASOUD_EXPDP 
